var display = "none";

function expandContent() {
  var dropdownnav = document.getElementById("dropdownnav");

  if (display == "none") {
    dropdownnav.style.display = "block";
    display = "block";
  } else {
    dropdownnav.style.display = "none";
    display = "none";
  }
}

